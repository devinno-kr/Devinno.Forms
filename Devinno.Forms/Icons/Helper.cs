        public static FAI GetFAI(string IconString)
        {
            FontFamily font = null;
            string icon = null;

            if (IconString != null)
            {
                var ls = IconString.ToLower().Replace("\t", " ").Split(' ').Where(x => x.Trim() != "").ToList();
                if (ls.Count == 2)
                {
                    var _icon = ls.LastOrDefault();
                    if (ls.FirstOrDefault() == "fab")
                    {
                        font = FontAwesome.Families[0];
                        if (dic["fab"].ContainsKey(_icon)) icon = char.ConvertFromUtf32(dic["fab"][_icon]);
                    }
                    else if (ls.FirstOrDefault() == "far")
                    {
                        font = FontAwesome.Families[1];
                        if (dic["far"].ContainsKey(_icon)) icon = char.ConvertFromUtf32(dic["far"][_icon]);
                    }
                    else if (ls.FirstOrDefault() == "fas")
                    {
                        font = FontAwesome.Families[2];
                        if (dic["fas"].ContainsKey(_icon)) icon = char.ConvertFromUtf32(dic["fas"][_icon]);
                    }
                }
                else if (ls.Count == 1)
                {
                    var _icon = ls.LastOrDefault();
                    font = FontAwesome.Families[2];
                    if (dic["fas"].ContainsKey(_icon)) icon = char.ConvertFromUtf32(dic["fas"][_icon]);
                }
            }

            if (font != null && icon != null) return new FAI(font, icon);
            else return null;
        }

        public static bool Valid(string IconString)
        {
            bool ret = false;
            if (IconString != null)
            {
                var ls = IconString.ToLower().Replace("\t", " ").Split(' ').Where(x => x.Trim() != "").ToList();
                if (ls.Count == 2)
                {
                    var _font = ls.FirstOrDefault();
                    var _icon = ls.LastOrDefault();
                    if (_font == "fab") ret = dic["fab"].ContainsKey(_icon);
                    else if (_font == "far") ret = dic["far"].ContainsKey(_icon);
                    else if (_font == "fas") ret = dic["fas"].ContainsKey(_icon);
                }
                else if (ls.Count == 1)
                {
                    var _icon = ls.LastOrDefault();
                    ret = dic["fas"].ContainsKey(_icon);
                }
            }
            return ret;
        }
